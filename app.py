## Age wise schemes

age=int(input("Enter a your age :"))

if age<19:
  print("You are not adult ")
elif (age>=19 and age<25):
  print("You have scheme B to apply")
elif (age>=25 and age<35):
  print("You have scheme B to apply")
elif (age>=35 and age<45):
  print("You have scheme B to apply")
else:
  print("You can't apply for any scheme.")
